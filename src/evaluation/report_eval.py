"""Report-level evaluation: disclaimer, counter-evidence, forbidden phrases.

These checks are designed for a Markdown research brief generated by
:class:`src.agents.report_agent.ReportAgent`. They are heuristic but
deterministic so the MVP can be regression-tested in CI.
"""

from __future__ import annotations

import re

from pydantic import BaseModel, ConfigDict, Field

FORBIDDEN_PHRASES: list[str] = [
    "buy now",
    "guaranteed",
    "must rise",
    "必然上涨",
    "强烈买入",
]

DISCLAIMER_TOKENS: tuple[str, ...] = (
    "disclaimer",
    "investment advice",
)

COUNTER_EVIDENCE_TOKENS: tuple[str, ...] = (
    "counter-evidence",
    "counter evidence",
    "risks and counter",
)

CITATION_PATTERN = re.compile(r"\[\d+\]")


class ReportEvalResult(BaseModel):
    """Aggregate report-quality checks for a Markdown research brief."""

    model_config = ConfigDict(extra="forbid")

    every_claim_has_citation: bool
    disclaimer_exists: bool
    counter_evidence_exists: bool
    no_forbidden_phrases: bool
    forbidden_phrases_found: list[str] = Field(default_factory=list)
    citation_marker_count: int = 0


def _has_disclaimer(text: str) -> bool:
    lowered = text.lower()
    return any(token in lowered for token in DISCLAIMER_TOKENS)


def _has_counter_evidence(text: str) -> bool:
    lowered = text.lower()
    return any(token in lowered for token in COUNTER_EVIDENCE_TOKENS)


def _find_forbidden(text: str) -> list[str]:
    """Return the subset of :data:`FORBIDDEN_PHRASES` present in ``text``."""
    lowered = text.lower()
    return [phrase for phrase in FORBIDDEN_PHRASES if phrase in lowered]


def evaluate_report(report_text: str) -> ReportEvalResult:
    """Compute report-quality checks for the supplied Markdown brief.

    The ``every_claim_has_citation`` flag uses a simple heuristic: the
    report must contain at least one ``[N]`` citation marker in its
    body. A more rigorous check (per-claim evidence linkage) lives in
    :mod:`src.agents.report_agent`.
    """
    citation_markers = CITATION_PATTERN.findall(report_text)
    citation_count = len(citation_markers)
    has_citation = citation_count > 0

    forbidden = _find_forbidden(report_text)

    return ReportEvalResult(
        every_claim_has_citation=has_citation,
        disclaimer_exists=_has_disclaimer(report_text),
        counter_evidence_exists=_has_counter_evidence(report_text),
        no_forbidden_phrases=not forbidden,
        forbidden_phrases_found=forbidden,
        citation_marker_count=citation_count,
    )


__all__ = [
    "COUNTER_EVIDENCE_TOKENS",
    "DISCLAIMER_TOKENS",
    "FORBIDDEN_PHRASES",
    "ReportEvalResult",
    "evaluate_report",
]
